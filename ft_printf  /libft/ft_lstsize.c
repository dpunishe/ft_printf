#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		size;
	t_list	*tmp;

	if (!lst)
		return (0);
	size = 1;
	tmp = lst;
	while (tmp->next)
	{
		size++;
		tmp = tmp->next;
	}
	return (size);
}
